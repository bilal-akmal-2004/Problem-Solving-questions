// Problem: Even or Odd
// Write a JavaScript program that checks if a number is even or odd.
// Example:
// Input: 4 → Output: Even
// Input: 7 → Output: Odd
// Hint: Use the modulus operator (%) to check the remainder when dividing by 2.
// Solution:

let a = 10;

if (a % 2 === 0) {
  console.log("Even");
} else {
  console.log("Odd");
}
